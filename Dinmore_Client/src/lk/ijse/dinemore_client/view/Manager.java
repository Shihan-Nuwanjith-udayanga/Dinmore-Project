    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            int eid = Integer.parseInt(lblEid.getText());
            String name = new String(Base64.getEncoder().encode(txtName.getText().getBytes()));
            String password = new String(Base64.getEncoder().encode(txtPassword.getText().getBytes()));

            String status = comboStatus.getSelectedItem().toString();

            dTO = new EmployeeDTO(eid, name, password, status);
            boolean addEmployee = controller.addEmployee(dTO);
            if (addEmployee) {
                JOptionPane.showMessageDialog(this, "Added Success...");
                getEmployeeID();
                loadAllEmployee();
                setClear();
            } else {
                JOptionPane.showMessageDialog(this, "Not Added...");
            }
        } catch (Exception ex) {
            Logger.getLogger(Manager.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnAddActionPerformed

