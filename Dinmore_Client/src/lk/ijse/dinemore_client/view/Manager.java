    private void loadAllEmployee() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) tblManager.getModel();
            dtm.setRowCount(0);
            EmployeeController controller = new EmployeeController();
            ArrayList<EmployeeDTO> allOrder = controller.getAllEmployee();
            for (EmployeeDTO i : allOrder) {
                Object[] row = {i.getE_id(), i.getE_name(), i.getE_passowrd(), i.getStatus()};
                dtm.addRow(row);
            }
        } catch (Exception ex) {
            Logger.getLogger(TelephoneOperatorForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

